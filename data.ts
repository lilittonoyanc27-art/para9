export interface QuizQuestion {
  question: string;
  options: string[];
  answer: string;
  context?: string;
}

export function getRandomQuizQuestions(count: number = 10): QuizQuestion[] {
  const pool: QuizQuestion[] = [];

  // Add haber forms
  pool.push({
    question: 'haber — yo ձևը Pretérito Perfecto-ում',
    options: ['he', 'has', 'ha', 'había'],
    answer: 'he'
  });
  pool.push({
    question: 'haber — nosotros ձևը Pretérito Perfecto-ում',
    options: ['hemos', 'habéis', 'han', 'habíamos'],
    answer: 'hemos'
  });
  pool.push({
    question: 'haber — ellos ձևը Pretérito Perfecto-ում',
    options: ['han', 'hemos', 'ha', 'habían'],
    answer: 'han'
  });

  // Add pluscuamperfecto haber forms
  pool.push({
    question: 'haber — yo/él ձևը Pluscuamperfecto-ում',
    options: ['había', 'habías', 'habíamos', 'he'],
    answer: 'había'
  });
  pool.push({
    question: 'haber — tú ձևը Pluscuamperfecto-ում',
    options: ['habías', 'había', 'habíais', 'has'],
    answer: 'habías'
  });

  // Add regular participio
  pool.push({
    question: 'խոսել (hablar) — սովորական participio-ն',
    options: ['hablado', 'hablito', 'hablió', 'hablado/a'],
    answer: 'hablado'
  });
  pool.push({
    question: 'ապրել (vivir) — սովորական participio-ն',
    options: ['vivido', 'vivado', 'vivió', 'vuelto'],
    answer: 'vivido'
  });

  // Add main participio exceptions
  PERFECTO_EXCEPTIONS.forEach(item => {
    pool.push({
      question: `"${item.latin}" բայի participio-ն (${item.meaning})`,
      options: [item.participle, item.latin + 'ado', item.latin + 'ido', 'vuelto'].filter((v, i, a) => a.indexOf(v) === i),
      answer: item.participle,
      context: item.example.spanish + ' — ' + item.example.armenian
    });
  });

  // Add prefix exceptions
  PREFIX_EXCEPTIONS.forEach(item => {
    pool.push({
      question: `"${item.verb}" բայի participio-ն (${item.meaning})`,
      options: [item.participle, item.verb + 'ado', item.verb + 'ido', 'dicho'],
      answer: item.participle
    });
  });

  // Add Imperfecto exceptions
  IMPERFECTO_EXCEPTIONS.forEach(item => {
    pool.push({
      question: `SER (լինել) բայի tú ձևը Imperfecto-ում`,
      options: ['era', 'eras', 'éramos', 'fui'],
      answer: 'eras'
    });
    pool.push({
      question: `IR (գնալ) բայի nosotros ձևը Imperfecto-ում`,
      options: ['íbamos', 'iba', 'ibas', 'fuimos'],
      answer: 'íbamos'
    });
    pool.push({
      question: `VER (տեսնել) բայի yo ձևը Imperfecto-ում`,
      options: ['veía', 'veías', 'veíamos', 'visto'],
      answer: 'veía'
    });
  });

  // Add Indefinido exceptions
  INDEFINIDO_EXCEPTIONS.forEach(item => {
    pool.push({
      question: `"${item.verb}" բայի él/ella ձևը Indefinido-ում (${item.meaning})`,
      options: [item.conjugations['él / ella'], item.conjugations['yo'], item.conjugations['ellos'], item.verb + 'ó'],
      answer: item.conjugations['él / ella']
    });
    pool.push({
      question: `"${item.verb}" բայի yo ձևը Indefinido-ում (${item.meaning})`,
      options: [item.conjugations['yo'], item.conjugations['tú'], item.conjugations['nosotros'], 'iba'],
      answer: item.conjugations['yo']
    });
  });

  // Shuffle and pick
  const shuffled = pool.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}
